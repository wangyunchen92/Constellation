- (NSString *) uniqueDeviceIdentifier{
    NSString *macaddress = [[UIDevice currentDevice] macaddress];
    NSString *bundleIdentifier = [[NSBundle mainBundle] bundleIdentifier];
    
    NSString *stringToHash = [NSString stringWithFormat:@"%@%@",macaddress,bundleIdentifier];
    NSString *uniqueIdentifier = [stringToHash stringFromMD5];
    
    return uniqueIdentifier;
}

- (NSString *) uniqueGlobalDeviceIdentifier{
    NSString *macaddress = [[UIDevice currentDevice] macaddress];
    NSString *uniqueIdentifier = [macaddress stringFromMD5];
    
    return uniqueIdentifier;
}

//获取smi 卡信息
+ (NSString *)getIMSI{
    
    CTTelephonyNetworkInfo *info = [[CTTelephonyNetworkInfo alloc] init];
    
    CTCarrier *carrier = [info subscriberCellularProvider];
   //国家编号
    NSString *CountryCode = [carrier mobileCountryCode];
    //网络供应商编码
    NSString *NetworkCode = [carrier mobileNetworkCode];
    
    if ([NetworkCode isEqualToString:@"00"] || [NetworkCode isEqualToString:@"02"] || [NetworkCode isEqualToString:@"07"]) {
        //移动
        NetworkCode = @"CMCC";
    }
    if ([NetworkCode isEqualToString:@"03"] || [NetworkCode isEqualToString:@"05"])
    {
        // ret = @"电信";
        NetworkCode =  @"CTCC";
    }
    if ([NetworkCode isEqualToString:@"01"] || [NetworkCode isEqualToString:@"06"])
    {
        // ret = @"联通";
        NetworkCode =  @"CUCC";
    }
    NSString *imsi = [NSString stringWithFormat:@"%@%@", CountryCode, NetworkCode];
    
    return imsi;
}

//获取平台
- (NSString *)platform{
    size_t size;
    sysctlbyname("hw.machine", NULL, &size, NULL, 0);
    char *machine = malloc(size);
    sysctlbyname("hw.machine", machine, &size, NULL, 0);
    NSString *platform = [NSString stringWithCString:machine encoding:NSUTF8StringEncoding];
    free(machine);
    return platform;
}
